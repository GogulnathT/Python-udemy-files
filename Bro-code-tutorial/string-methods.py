name = "gogul"

print("Length:",len(name))
print("finding the index of 'u' using .find:",name.find("u"))
print(".capitalize",name.capitalize())
print("uppercase using .upper:",name.upper())
print("lowercase using .lower:",name.lower())
print(".isdigit:",name.isdigit()) #checks whether the string is digits only
print(".isalpha:",name.isalpha()) #checks if alphabet only
print("counting using .count:", name.count("g")) 
print(".replace('g','l'):",name.replace('g','l'))
print(name*3) #displays the string multiple times